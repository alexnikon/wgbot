@app.post("/webhook/yookassa")
async def yookassa_webhook(request: Request):
    """–û–±—Ä–∞–±–æ—Ç—á–∏–∫ webhook –æ—Ç –ÆKassa"""
    try:
        # –ü–æ–ª—É—á–∞–µ–º —Ç–µ–ª–æ –∑–∞–ø—Ä–æ—Å–∞
        body = await request.body()
        body_str = body.decode('utf-8')
        
        # –ü–æ–ª—É—á–∞–µ–º –ø–æ–¥–ø–∏—Å—å –∏–∑ –∑–∞–≥–æ–ª–æ–≤–∫–æ–≤
        signature = request.headers.get('X-YooMoney-Signature', '')
        
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º –ø–æ–¥–ø–∏—Å—å
        if not yookassa_client.verify_webhook_signature(body_str, signature):
            logger.warning("–ù–µ–≤–µ—Ä–Ω–∞—è –ø–æ–¥–ø–∏—Å—å webhook –æ—Ç –ÆKassa")
            raise HTTPException(status_code=400, detail="Invalid signature")
        
        # –ü–∞—Ä—Å–∏–º –¥–∞–Ω–Ω—ã–µ
        webhook_data = yookassa_client.parse_webhook(body_str)
        if not webhook_data:
            logger.error("–û—à–∏–±–∫–∞ –ø–∞—Ä—Å–∏–Ω–≥–∞ webhook")
            raise HTTPException(status_code=400, detail="Invalid JSON")
        
        # –ü–æ–ª—É—á–∞–µ–º –¥–∞–Ω–Ω—ã–µ –ø–ª–∞—Ç–µ–∂–∞
        payment_data = webhook_data.get('object', {})
        payment_status = payment_data.get('status')
        
        logger.info(f"–ü–æ–ª—É—á–µ–Ω webhook: —Å—Ç–∞—Ç—É—Å {payment_status}, ID {payment_data.get('id')}")
        
        # –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –≤ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–∏ –æ—Ç —Å—Ç–∞—Ç—É—Å–∞
        if yookassa_client.is_payment_succeeded(payment_data):
            await process_successful_payment(payment_data)
        elif yookassa_client.is_payment_canceled(payment_data):
            await process_canceled_payment(payment_data)
        else:
            logger.info(f"–ü–ª–∞—Ç–µ–∂ –≤ —Å—Ç–∞—Ç—É—Å–µ: {payment_status}")
        
        return JSONResponse(content={"status": "ok"})
        
    except HTTPException:
        raise
    except Exception as e:
        logger.error(f"–û—à–∏–±–∫–∞ –æ–±—Ä–∞–±–æ—Ç–∫–∏ webhook: {e}")
        raise HTTPException(status_code=500, detail="Internal server error")

